/**
 * k6 Load Test Suite — Agent Registry
 *
 * Tests:
 *  1. Discovery & read endpoints (GET /discover, GET /agents/:id)
 *  2. Job lifecycle under concurrency (propose → accept → fund → deliver → verify)
 *  3. Rate limiting validation (burst beyond limits, expect 429s)
 *  4. Concurrent escrow operations (stress row-level locking)
 *
 * Prerequisites:
 *  1. Run setup.py to create test agents and test-data.json
 *  2. Start signer.py proxy (handles Ed25519 signing for k6)
 *  3. Run: k6 run load-tests/main.js
 *
 * Architecture:
 *  k6 → signer proxy (:9999) → API (:8080 or staging)
 *  The signer proxy adds Ed25519 signatures to each request.
 */

import http from 'k6/http';
import { check, group, sleep } from 'k6';
import { Counter, Rate, Trend } from 'k6/metrics';
import { SharedArray } from 'k6/data';

// ─── Configuration ───

const SIGNER_URL = __ENV.SIGNER_URL || 'http://127.0.0.1:9999';

// Load test data generated by setup.py
const testData = JSON.parse(open('./test-data.json'));
const agents = new SharedArray('agents', () => testData.agents);
const listings = new SharedArray('listings', () => testData.listings);
const buyerIds = testData.buyers;
const sellerIds = testData.sellers;

// ─── Custom Metrics ───

const rateLimitHits = new Counter('rate_limit_hits');
const escrowOps = new Counter('escrow_operations');
const jobsCreated = new Counter('jobs_created');
const jobsCompleted = new Counter('jobs_completed');
const errorRate = new Rate('error_rate');
const discoveryDuration = new Trend('discovery_duration', true);
const jobLifecycleDuration = new Trend('job_lifecycle_duration', true);

// ─── Test Scenarios ───

