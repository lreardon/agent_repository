// ─── Helpers ───

function getAgent(agentId) {
  return agents.find(a => a.agent_id === agentId);
}

function randomBuyer() {
  const id = buyerIds[Math.floor(Math.random() * buyerIds.length)];
  return getAgent(id);
}

function randomSeller() {
  const id = sellerIds[Math.floor(Math.random() * sellerIds.length)];
  return getAgent(id);
}

function randomListing() {
  return listings[Math.floor(Math.random() * listings.length)];
}

function signedHeaders(agent) {
  return {
    'Content-Type': 'application/json',
    'X-Agent-Id': agent.agent_id,
    'X-Private-Key': agent.private_key,
  };
}

function apiGet(path, agent) {
  return http.get(`${SIGNER_URL}${path}`, { headers: signedHeaders(agent) });
}

function apiPost(path, body, agent) {
  return http.post(`${SIGNER_URL}${path}`, JSON.stringify(body), {
    headers: signedHeaders(agent),
  });
}

// ─── Scenario 1: Read Endpoints ───

export function readEndpoints() {
  const agent = agents[Math.floor(Math.random() * agents.length)];

  group('discovery', () => {
    const start = Date.now();
    const res = apiGet('/v1/discover?skill_id=load-test', agent);
    discoveryDuration.add(Date.now() - start);

    const ok = check(res, {
      'discover status 200': (r) => r.status === 200,
      'discover returns items': (r) => {
        const body = r.json();
        return body && Array.isArray(body.items);
      },
    });
    errorRate.add(!ok);
  });

  group('agent_lookup', () => {
    // Look up a random other agent
    const other = agents[Math.floor(Math.random() * agents.length)];
    const res = apiGet(`/v1/agents/${other.agent_id}`, agent);

    const ok = check(res, {
      'agent lookup status 200': (r) => r.status === 200,
      'agent has display_name': (r) => r.json().display_name !== undefined,
    });
    errorRate.add(!ok);
  });

  group('listings', () => {
    const res = apiGet('/v1/listings?limit=10', agent);

    const ok = check(res, {
      'listings status 200': (r) => r.status === 200,
    });
    errorRate.add(!ok);
  });

  sleep(0.1);  // Small pause between iterations
}

// ─── Scenario 2: Job Lifecycle ───

export function jobLifecycle() {
  const buyer = randomBuyer();
  const listing = randomListing();
  const seller = getAgent(listing.seller_id);

  if (!buyer || !seller) return;

  const start = Date.now();
  let jobId;

  group('propose_job', () => {
    const res = apiPost('/v1/jobs', {
      seller_agent_id: seller.agent_id,
      listing_id: listing.listing_id,
      max_budget: '5.00',
      acceptance_criteria: null,
      requirements: { task: 'load-test-task' },
      delivery_deadline: new Date(Date.now() + 3600000).toISOString(),
      max_rounds: 3,
    }, buyer);

    const ok = check(res, {
      'job created': (r) => r.status === 201,
    });

    if (res.status === 201) {
      jobId = res.json().job_id;
      jobsCreated.add(1);
    }
    errorRate.add(!ok);
  });

  if (!jobId) return;

  sleep(0.2);

  group('accept_job', () => {
    const res = apiPost(`/v1/jobs/${jobId}/accept`, {}, seller);
    check(res, { 'job accepted': (r) => r.status === 200 });
  });

  sleep(0.2);

  group('fund_job', () => {
    const res = apiPost(`/v1/jobs/${jobId}/fund`, {}, buyer);
    const ok = check(res, { 'job funded': (r) => r.status === 200 || r.status === 201 });
    if (ok) escrowOps.add(1);
  });

  sleep(0.2);

  group('start_job', () => {
    const res = apiPost(`/v1/jobs/${jobId}/start`, {}, seller);
    check(res, { 'job started': (r) => r.status === 200 });
  });

  sleep(0.2);

  group('deliver_job', () => {
    const res = apiPost(`/v1/jobs/${jobId}/deliver`, {
      result: { output: 'load-test-output', data: [1, 2, 3] },
    }, seller);
    check(res, { 'job delivered': (r) => r.status === 200 });
  });

  sleep(0.2);

  group('complete_job', () => {
    // Complete without verification (no acceptance_criteria set)
    const res = apiPost(`/v1/jobs/${jobId}/complete`, {}, buyer);
    const ok = check(res, { 'job completed': (r) => r.status === 200 });
    if (ok) {
      jobsCompleted.add(1);
      escrowOps.add(1);
    }
  });

  jobLifecycleDuration.add(Date.now() - start);
  sleep(1);  // Pause between full lifecycle runs
}

// ─── Scenario 3: Rate Limit Validation ───

export function rateLimitTest() {
  const agent = agents[__VU % agents.length];

  group('burst_discovery', () => {
    let hit429 = false;

    // Fire 100 rapid requests — should trigger rate limit (60 tokens for discovery)
    for (let i = 0; i < 100; i++) {
      const res = apiGet('/v1/discover?skill_id=load-test', agent);

      if (res.status === 429) {
        hit429 = true;
        rateLimitHits.add(1);

        check(res, {
          'rate limit returns 429': (r) => r.status === 429,
          'rate limit has retry-after header': (r) =>
            r.headers['Retry-After'] !== undefined ||
            r.headers['retry-after'] !== undefined,
        });
        break;
      }
    }

    check(null, {
      'rate limiter triggered': () => hit429,
    });
  });

  sleep(5);  // Let tokens refill

  group('burst_writes', () => {
    let hit429 = false;

    // Fire rapid write requests — should trigger write rate limit (30 tokens)
    for (let i = 0; i < 50; i++) {
      const listing = randomListing();
      const res = apiPost('/v1/jobs', {
        seller_agent_id: listing.seller_id,
        listing_id: listing.listing_id,
        max_budget: '1.00',
        requirements: { task: 'rate-limit-test' },
        delivery_deadline: new Date(Date.now() + 3600000).toISOString(),
      }, agent);

      if (res.status === 429) {
        hit429 = true;
        rateLimitHits.add(1);
        break;
      }
    }

    check(null, {
      'write rate limiter triggered': () => hit429,
    });
  });
}

// ─── Scenario 4: Concurrent Escrow Stress ───

export function escrowStress() {
  // Multiple VUs create and fund jobs simultaneously to stress row-level locking
  const buyer = randomBuyer();
  const listing = randomListing();
  const seller = getAgent(listing.seller_id);

  if (!buyer || !seller) return;

  // Create job
  const createRes = apiPost('/v1/jobs', {
    seller_agent_id: seller.agent_id,
    listing_id: listing.listing_id,
    max_budget: '2.00',
    requirements: { task: 'escrow-stress' },
    delivery_deadline: new Date(Date.now() + 3600000).toISOString(),
    max_rounds: 1,
  }, buyer);

  if (createRes.status !== 201) return;
  const jobId = createRes.json().job_id;
  jobsCreated.add(1);

  sleep(0.1);

  // Accept
  apiPost(`/v1/jobs/${jobId}/accept`, {}, seller);
  sleep(0.1);

  // Fund (this is the critical escrow operation)
  const fundRes = apiPost(`/v1/jobs/${jobId}/fund`, {}, buyer);
  const funded = check(fundRes, {
    'escrow funded under contention': (r) => r.status === 200 || r.status === 201,
  });

  if (funded) {
    escrowOps.add(1);

    sleep(0.1);

    // Start
    apiPost(`/v1/jobs/${jobId}/start`, {}, seller);
    sleep(0.1);

    // Deliver
    apiPost(`/v1/jobs/${jobId}/deliver`, {
      result: { output: 'stress-test' },
    }, seller);
    sleep(0.1);

    // Complete
    const completeRes = apiPost(`/v1/jobs/${jobId}/complete`, {}, buyer);
    if (completeRes.status === 200) {
      jobsCompleted.add(1);
      escrowOps.add(1);
    }
  }

  // Check balance consistency
  const balanceRes = apiGet(`/v1/agents/${buyer.agent_id}/balance`, buyer);
  check(balanceRes, {
    'balance endpoint healthy': (r) => r.status === 200,
    'balance is non-negative': (r) => parseFloat(r.json().balance) >= 0,
  });

  sleep(0.5);
}
